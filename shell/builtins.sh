# =================================== shopt ================================== #

# shopt 设置 shell 选项
shopt        # 打印所有配置
shopt -p     # 同上
shopt -o opt # 修改使用 set -o 指定的配置
shopt -s opt # 打开配置
shopt -u opt # 关闭配置
shopt -q opt # 判断某个配置是否打开

dotglob    # 文件扩展的时候匹配 . 开头的隐藏文件
nullglob   # 文件扩展的时候，如果没有匹配到文件，则扩展为空字符串
failglob   # 文件扩展的时候，如果没有匹配到文件，则报错
nocaseglob # 文件扩展的时候忽略大小写

